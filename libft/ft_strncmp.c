#include <libft.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t i;

	if ((NULL == s1) || (NULL == s2))
	{
		return (INT_MIN);
	}
	i = 0;
	while ((i < n) && (s1[i] || s2[i]))
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
