#include <libft/std.h>

uint32_t ft_hash(const unsigned char *key, size_t keysize)
{
	uint32_t  hash;

	hash = 0;
	while (keysize--)
	{
		hash += *key++;
		hash += hash << 10;
		hash ^= hash >> 6;
	}
	hash += hash << 3;
	hash ^= hash >> 11;
	hash += hash << 15;

	return (hash);
}
